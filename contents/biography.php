<h2>Biography</h2>
<p>Vortech was founded in January 2000 by Juha Untinen and Tero Konola. The original style was decidedly more traditional heavy metal, with added electronic elements. Over the years the style would evolve into more extreme forms of metal, drawing influence from groups such as Fear Factory, The Amenta and Sonic Mayhem. The current sound can be described as Industrial Death Metal.</p>
<p>Prior to 2009, the group had played live only once, but starting from summer 2009, a number of shows were performed as a two-piece, consisting of Juha Untinen and Matti S채rkim채ki. With the aid of technology, the pair provided an energetic show worthy of a full 5-piece group in sonic qualities, performing to much intrigued audiences in Northern Finland.</p>
<p>To coincide with active live performing, the first three full length releases were self-released on CD, along with other merchandise, and were sold at a steady pace. In late 2010, Ville Miinala joined the band to play drums, first appearing on record with the release of the latest album, Devoid of Life. The album also features the new vocalist, Mikko Nikula, who joined Vortech in early 2012. At the moment, the band is rehearsing for live shows and working on several releases, set to be out in 2013!</p>
