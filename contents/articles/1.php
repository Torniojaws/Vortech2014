		<h2>Reflections of the Past: Extinct</h2>
		<h3 class="subtitle">Reminiscing the days when Extinct was recorded</h3>
		<p>Studio Diary by Juha Untinen, written 11 Feb 2007</p>
	</header>
	<section class="fulltext">
		<p>Extinct is an album featuring re-recorded and re-arranged versions of several classic SOUND OGRE songs. The songs date from between the years 2000 and 2002, however not much of the arrangements remain from those versions. I started working on this release right after I moved into a new house and I was bored waiting for the internet connection to be redirected to my new house. So, I started fiddling around with these songs, and eventually I decided to release them as a proper EP. It was a fun project and I think all the songs received a well-needed retouch to make them better.</p>
		<p>All the songs were done on Acid 1.0 and Reason 2.5. Each song had two guitar tracks, panned about 80% left and right. The bass tracks were done with Reason. All drums were done on Acid with various samples from all over. The majority of all electronics were done with Reason, thought there are some small things that I did with Fruity Loops 5.</p>
	</section>
