		<h2>Reflections of the Past: Asphyxiation</h2>
		<h3 class="subtitle">Going back in time to the studio</h3>
		<p>Studio Report by Juha Untinen, written 11 Feb 2007</p>
	</header>
	<section class="fulltext">
		<p>This look back in time involves the second EP, which was also the last release as SOUND OGRE.</p>
		<p>I began composing the songs for this album in early 2005. The first completed song was Spiritual Entity, or "The Entity" as it was called back then. Then later on a few more songs were finished, leading to the eventual release of the EP. Most of the songs were added to Mikseri.net as they were completed, and they all received a good response. The songs were initially intended to be instrumental, but after so many requests I decided I'd do some vocals to the songs too. So that's how they are today. The remaining songs were written during a timespan of Jan 2005 to March 2005.</p>
		<p>Technically the album was one the more advanced compositions I had done until then. All songs featured quad-tracked guitars, intricate bass lines done with Reason 2.5 and the most complex drumming I had done with Acid 4. Yes, the songs were done on the classic Acid :) It wasn't too bad of a program during my skill level those days. But this was the last release I did with it before moving on. All the electronics were done with both Reason 2.5 and Fruity Loops 5. Programming the drums was quite fun with Acid, even compared to modern ways. To do a 16th note doublekick line, you simply chose 16th note and painted the drums in one sweep. Now you have to place each note separately (luckily there's copypaste to help you :P).</p>
	</section>
